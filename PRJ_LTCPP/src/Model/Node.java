package Model;

public class Node {
	
	//the super class of User and Workplace
	
	private int numUser; //Information relevant to a user not a node
	private double x;
	private double y;
	/**
	 * @return the y
	 */
	public double getY() {
		return y;
	}
	/**
	 * @param y the y to set
	 */
	public void setY(double y) {
		this.y = y;
	}
	/**
	 * @return the numUser
	 */
	public int getNumUser() {
		return numUser;
	}
	/**
	 * @param numUser the numUser to set
	 */
	public void setNumUser(int numUser) {
		this.numUser = numUser;
	}
	/**
	 * @return the x
	 */
	public double getX() {
		return x;
	}
	/**
	 * @param x the x to set
	 */
	public void setX(double x) {
		this.x = x;
	}

}
